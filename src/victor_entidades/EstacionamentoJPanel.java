    private void atualizaTabela(){
        List<Estacionamento> lista = dao.listar();
        DefaultTableModel dfm = (DefaultTableModel) jTableEstacionamento.getModel();
        int linhas = jTableEstacionamento.getRowCount();
        for(int i = 0; i < linhas; i++){
            dfm.removeRow(0);   
        }
        
        for (Estacionamento estacionamento : lista){
            Object[] linha = new Object[]{estacionamento.getCpf(), estacionamento.getNome(), estacionamento.getCod_vaga(), estacionamento.getPreco(), estacionamento.getHora_entrada(), estacionamento.getHora_saida()};
            dfm.addRow(linha);
            //JOptionPane.showMessageDialog(jButtonSalvar, linha);
        }
    }
    
