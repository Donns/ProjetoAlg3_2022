    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        Estacionamento estacionamento = new Estacionamento();   
        try{
            //JOptionPane.showMessageDialog(jButtonSalvar, "try");
            String cpf = CpfTextField.getText();
            estacionamento.setCpf(cpf);
            estacionamento.setNome(NomeTextField.getText());
            estacionamento.setCod_vaga(Integer.parseInt(CodVagaTextField.getText()));
            estacionamento.setPreco(Float.parseFloat(PrecoTextField.getText()));
            estacionamento.setHora_entrada(EntradaTextField.getText());
            estacionamento.setHora_saida(SaidaTextField.getText());
            dao.editar(estacionamento);
        }catch(NumberFormatException e){
            //JOptionPane.showMessageDialog(jButtonSalvar, "catch");
            estacionamento.setCpf(CpfTextField.getText());
            estacionamento.setNome(NomeTextField.getText());
            estacionamento.setCod_vaga(Integer.parseInt(CodVagaTextField.getText()));
            estacionamento.setPreco(Float.parseFloat(PrecoTextField.getText()));
            estacionamento.setHora_entrada(EntradaTextField.getText());
            estacionamento.setHora_saida(SaidaTextField.getText());
            dao.adicionar(estacionamento);
        }
        JOptionPane.showMessageDialog(jButtonSalvar, "Salvo com sucesso!");
        atualizaTabela();
        CpfTextField.setEnabled(true);
        EntradaTextField.setEnabled(true);
        jButtonCancelarActionPerformed(evt);     
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        if(jTableEstacionamento.getSelectedRowCount() == 1){
            int linha = jTableEstacionamento.getSelectedRow();
            CpfTextField.setEnabled(false);
            EntradaTextField.setEnabled(false);
            
            CpfTextField.setText(jTableEstacionamento.getValueAt(linha, 0)+"");
            NomeTextField.setText(jTableEstacionamento.getValueAt(linha, 1)+"");
            CodVagaTextField.setText(jTableEstacionamento.getValueAt(linha, 2)+"");
            PrecoTextField.setText(jTableEstacionamento.getValueAt(linha, 3)+"");
            EntradaTextField.setText(jTableEstacionamento.getValueAt(linha, 4)+"");
            SaidaTextField.setText(jTableEstacionamento.getValueAt(linha, 5)+"");
        }else{
            JOptionPane.showMessageDialog(jButtonSalvar, "Selecione apenas uma linha!");
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        if (jTableEstacionamento.getSelectedRowCount() >= 1){
            int[] linhas = jTableEstacionamento.getSelectedRows();
            for (int i = linhas.length - 1; i >= 0; i--){
                String cpf = jTableEstacionamento.getValueAt(linhas[i], 0)+"";
                dao.remover(cpf);
            }
            JOptionPane.showMessageDialog(jButtonSalvar, "Removido com sucesso!");
            atualizaTabela();
        }else{
            JOptionPane.showMessageDialog(jButtonSalvar, "Selecione ao menos uma linha!");
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

