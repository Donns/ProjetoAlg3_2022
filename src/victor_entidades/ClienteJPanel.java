    private String data(){
        String format = "";
        String[] data = NascTextField.getText().split("");
        for (int i = 0; i < 8; i++){
            if (i == 2 || i == 4){
                format += "/";
            }
            format += data[i];
        }    
        return format;
    }
    
    
    private void atualizaTabela(){
        List<Cliente> lista = dao.listar();
        DefaultTableModel dfm = (DefaultTableModel) jTableClientes1.getModel();
        int linhas = jTableClientes1.getRowCount();
        for(int i = 0; i < linhas; i++){
            dfm.removeRow(0);   
        }
        
        for (Cliente cli : lista){
            Object[] linha = new Object[]{cli.getCli().getCpf(), cli.getCli().getNome(), cli.getCli().getTelefone(), cli.getCli().getData_nasc(), cli.getVendas_fidelidade()};
            dfm.addRow(linha);
            //JOptionPane.showMessageDialog(jButtonSalvar, linha);
        }
    }
    
