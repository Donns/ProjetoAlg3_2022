    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        Pessoa pessoa = new Pessoa();   
        Cliente cli = new Cliente();
        try{
            //JOptionPane.showMessageDialog(jButtonSalvar, "try");
            String fidelidade = FidelidadeTextField.getText();
            cli.setVendas_fidelidade(Integer.parseInt(fidelidade));
            pessoa.setCpf(CpfTextField.getText());
            pessoa.setNome(NomeTextField.getText());
            pessoa.setTelefone(TelTextField.getText());
            pessoa.setData_nasc(NascTextField.getText());
            cli.setCli(pessoa);
            dao.editar(cli);
        }catch(NumberFormatException e){
            //JOptionPane.showMessageDialog(jButtonSalvar, "catch");
            pessoa.setCpf(CpfTextField.getText());
            pessoa.setNome(NomeTextField.getText());
            pessoa.setTelefone(TelTextField.getText());
            if(NascTextField.getText().length() < 9){
                pessoa.setData_nasc(data());
            }else{
                pessoa.setData_nasc(NascTextField.getText());
            }
            cli.setCli(pessoa);
            cli.setVendas_fidelidade(0);
            dao.adicionar(cli);
        }
        JOptionPane.showMessageDialog(jButtonSalvar, "Salvo com sucesso!");
        atualizaTabela();
        CpfTextField.setEnabled(true);
        NascTextField.setEnabled(true);
        jButtonCancelarActionPerformed(evt);     
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        if(jTableClientes1.getSelectedRowCount() == 1){
            int linha = jTableClientes1.getSelectedRow();
            CpfTextField.setEnabled(false);
            NascTextField.setEnabled(false);
            FidelidadeTextField.setEnabled(false);
            
            CpfTextField.setText(jTableClientes1.getValueAt(linha, 0)+"");
            NomeTextField.setText(jTableClientes1.getValueAt(linha, 1)+"");
            TelTextField.setText(jTableClientes1.getValueAt(linha, 2)+"");
            NascTextField.setText(jTableClientes1.getValueAt(linha, 3)+"");
            FidelidadeTextField.setText(jTableClientes1.getValueAt(linha, 4)+"");    
        }else{
            JOptionPane.showMessageDialog(jButtonSalvar, "Selecione apenas uma linha!");
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        if (jTableClientes1.getSelectedRowCount() >= 1){
            int[] linhas = jTableClientes1.getSelectedRows();
            for (int i = linhas.length - 1; i >= 0; i--){
                String cpf = jTableClientes1.getValueAt(linhas[i], 0)+"";
                dao.remover(cpf);
            }
            JOptionPane.showMessageDialog(jButtonSalvar, "Removido com sucesso!");
            atualizaTabela();
        }else{
            JOptionPane.showMessageDialog(jButtonSalvar, "Selecione ao menos uma linha!");
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed


